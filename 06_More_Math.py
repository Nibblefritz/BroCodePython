# importing the math module to use its functions
import math

print(f"Value of pi is {math.pi}")  # The value of pi
print(f"Value of e is {math.e}")    # The value of e (exponential constant)

result = math.sqrt(16)  # Square root of 16
print(f"Square root of 16 is {result}")

# Factorial is the product of all positive integers up to a given number
result = math.factorial(5)  # Factorial of 5
print(f"Factorial of 5 is {result}")

result = math.ceil(9.1)  # Rounding up to the nearest integer
print(f"Ceiling of 9.1 is {result}")
result = math.floor(9.9)  # Rounding down to the nearest integer
print(f"Floor of 9.9 is {result}")